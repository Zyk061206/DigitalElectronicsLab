module encoder_tb(

    );
    reg [7:0] I;
    wire [2:0] O1,O2,O3;
    Encoder8to3_1 ut1(I,O1);
    Encoder8to3_2 ut2(I,O2);
    Encoder8to3_3 ut3(I,O3);
    
    // 测试过程
    initial begin
        // 初始化输入
        I = 8'b1111_1111;
                
        // 测试用例 1: 无有效输入
        #10 I = 8'b1111_1111;
        
        // 测试用例 2: 单个有效输入
        #10 I = 8'b1111_1110;  // I0有效
        
        #10 I = 8'b1111_1101;  // I1有效
        
        #10 I = 8'b1111_1011;  // I2有效
        
        #10 I = 8'b1111_0111;  // I3有效
        
        #10 I = 8'b1110_1111;  // I4有效
        
        #10 I = 8'b1101_1111;  // I5有效
        
        #10 I = 8'b1011_1111;  // I6有效
        
        #10 I = 8'b0111_1111;  // I7有效
        
        // 测试用例 3: 多个有效输入 - 测试优先级
        #10 I = 8'b0111_1110;  // I7和I0同时有效
        
        #10 I = 8'b1011_1101;  // I6和I1同时有效
        
        #10 I = 8'b1101_1011;  // I5和I2同时有效
        
        #10 I = 8'b1110_0111;  // I4和I3同时有效
        
        // 测试用例 4: 三个以上有效输入
        #10 I = 8'b0110_1101;  // I7,I6,I3,I1有效
        
        #10 I = 8'b1010_1010;  // I6,I4,I2,I0有效
        
        // 测试用例 5: 所有输入有效
        #10 I = 8'b0000_0000;
        
        // 测试用例 6: 随机测试
        #10 I = 8'b1100_0011;
        
        #10 I = 8'b0011_1100;
        
        #10 I = 8'b1001_0110;
        
        $finish;
    end
endmodule
